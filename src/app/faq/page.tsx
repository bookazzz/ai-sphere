export default function FaqPage() {
  const [openItem, setOpenItem] = useState<{cat: number; idx: number} | null>(null);

  const toggleItem = (catIdx: number, itemIdx: number) => {
    if (openItem?.cat === catIdx && openItem?.idx === itemIdx) {
      setOpenItem(null);
    } else {
      setOpenItem({cat: catIdx, idx: itemIdx});
    }
  };

  // JSON-LD for FAQ schema
  const faqSchema = {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    mainEntity: faqData.flatMap(cat =>
      cat.items.map(item => ({
        '@type': 'Question',
        name: item.q,
        acceptedAnswer: {
          '@type': 'Answer',
          text: item.a,
        },
      }))
    ),
  };

  return (
    <>
      <script
        type="application/ld+json"
        dangerouslySetInnerHTML={{ __html: JSON.stringify(faqSchema) }}
      />

      {/* Header */}
      <header className="header">
        <div className="header__container">
          <Link href="/" className="header__logo">AI Sphere</Link>
          <nav className="header__menu">
            <Link href="/" className="header__link">Чат</Link>
            <Link href="/prices" className="header__link">Цены</Link>
            <Link href="/security" className="header__link">Безопасность</Link>
            <Link href="/faq" className="header__link header__link--active">FAQ</Link>
          </nav>
        </div>
      </header>

      {/* Hero */}
      <section className="faq-hero">
        <div className="faq-hero__container">
          <h1 className="faq-hero__title">Часто задаваемые вопросы</h1>
          <p className="faq-hero__subtitle">
            Всё, что нужно знать об AI Sphere: выбор модели, оплата, возможности и технические детали.
          </p>
        </div>
      </section>

      {/* FAQ List */}
      <section className="faq-section">
        <div className="faq-section__container">
          {faqData.map((category, catIdx) => (
            <div key={catIdx} className="faq-section__category">
              <h2 className="faq-section__category-title">{category.category}</h2>
              <div className="faq-section__list">
                {category.items.map((item, itemIdx) => {
                  const isOpen = openItem?.cat === catIdx && openItem?.idx === itemIdx;
                  return (
                    <div
                      key={itemIdx}
                      className={`faq-section__item ${isOpen ? 'faq-section__item--open' : ''}`}
                    >
                      <button
                        className="faq-section__question"
                        onClick={() => toggleItem(catIdx, itemIdx)}
                        aria-expanded={isOpen}
                      >
                        <span>{item.q}</span>
                        <span className="faq-section__icon">
                          {isOpen ? '−' : '+'}
                        </span>
                      </button>
                      <div
                        className="faq-section__answer"
                        hidden={!isOpen}
                      >
                        <p>{item.a}</p>
                      </div>
                    </div>
                  );
                })}
              </div>
            </div>
          ))}
        </div>
      </section>

      {/* Still have questions CTA */}
      <section className="faq-cta">
        <div className="faq-cta__container">
          <h2 className="faq-cta__title">Остались вопросы?</h2>
          <p className="faq-cta__text">
            Напишите нам — поможем разобраться с любой задачей.
          </p>
          <Link href="/contacts" className="faq-cta__btn">
            Связаться с поддержкой
          </Link>
        </div>
      </section>

      {/* Footer */}
      <Footer />
    </>
  );
}
